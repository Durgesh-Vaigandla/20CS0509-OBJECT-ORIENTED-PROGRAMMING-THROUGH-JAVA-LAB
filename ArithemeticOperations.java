public class ArithemeticOperations {
    public static void main(String[] args) {
        System.out.println("Arithemetic Operations:-");
        System.out.println("5 + 2 = "+(5+2));
        System.out.println("5 - 2 = "+(5-2));
        System.out.println("5 * 2 = "+(5*2));
        System.out.println("5 / 2 = "+(5/2));
        System.out.println("5 % 2 = "+(5%2));
    }
}
